pub enum OpenMode {
    /// Open if exists, create if not.
    Auto,
    /// Always create — return an error if the file already exists.
    ForceNew,
}

/// Run a Lua script (source string) against a database file non-interactively.
/// The script executes inside the same sandbox as the REPL. Output comes from
/// explicit `print()` calls only — there is no auto-printing of expressions.
pub fn exec_script(
    path: &Path,
    script: &str,
    mode: OpenMode,
    use_wal: bool,
    use_mmap: bool,
) -> Result<(), Box<dyn std::error::Error>> {
    let exists = path.exists();
    if matches!(mode, OpenMode::ForceNew) && exists {
        return Err(format!("'{}' already exists (omit --new to open it)", path.display()).into());
    }
    if use_mmap {
        let store = if exists { GraphStore::open_mmap(path)? } else { GraphStore::create_mmap(path)? };
        exec_inner(Rc::new(RefCell::new(store)), script)
    } else if use_wal {
        let store = if exists { GraphStore::open_wal(path)? } else { GraphStore::create_wal(path)? };
        exec_inner(Rc::new(RefCell::new(store)), script)
    } else {
        let store = if exists { GraphStore::open(path)? } else { GraphStore::create(path)? };
        exec_inner(Rc::new(RefCell::new(store)), script)
    }
}

